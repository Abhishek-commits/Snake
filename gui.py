    def create_image(self):
        screen_size = 441
        screen = pygame.Surface((screen_size, screen_size), pygame.SRCALPHA, 32)
        rect_size = 10
        x = 1
        y = 1
        for i in self.level:
            if i == 0:
                pygame.draw.rect(screen, pygame.Color("Grey"), pygame.Rect(x, y, rect_size, rect_size))
            x += rect_size + 1
            if x == screen_size:
                y += rect_size + 1
                x = 1
        pygame.image.save(screen, "images/gui.png")

    def draw_level(self, window):
        window.blit(self.gui_image, (0, 0))

    def init_field(self):
        x = 1
        y = 1
        for i in self.level:
            if i == 0:
                self.barrier.append([x, y])
            elif i == 1 and y != 12:
                self.field.append([x, y])
            x += 11
            if x == 441:
                y += 11
                x = 1

    def get_new_progress(self):
        self.progress.append([self.progress[-1][0] + 11, 12])

    def draw_progress(self, window):
        for i in self.progress:
            pygame.draw.rect(window, pygame.Color("Green"), pygame.Rect(i[0], i[1], 10, 10))

    def draw_win(self, window):
        window.blit(self.win_image, (23, 100))

    def draw_lose(self, window):
        window.blit(self.lose_image, (23, 100))

    def check_win_or_lose(self):
        if len(self.progress) == 0:
            self.game = "LOSE"
        elif len(self.progress) == 37:
            self.game = "WIN"
